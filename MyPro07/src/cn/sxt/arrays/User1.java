package cn.sxt.arrays;

public class User1{
	private int id;
	private String name;
	public int getId() {
		return id;
	}
	
	public User1(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}